void jh_update_vec(int vec[jh_type_cnt], char ch) {
    for (int i = 0; i < jh_type_cnt; i++) {
        vec[i] = jh_dtrans[i][vec[i]][(int)ch];
    }
}

int jh_detect_type(const int vec[jh_type_cnt]) {
    bool not_zero = false;
    for (int i = 0; i < jh_type_cnt; i++) {
        int state = vec[i];
        if (state != 0) not_zero = true;
        if (std::find(jh_target_bucket[i].cbegin(), jh_target_bucket[i].cend(), state)
            != jh_target_bucket[i].cend())
            return i;
    }
    if (not_zero)
        return -2;
    else
        return -1;
}

char *jh_get_lexeme(char *b, int *type) {
    int curr_vec[jh_type_cnt];
    char *f = b, *old_f = b;
    int old_type = -1, new_type = -1;
    for (int &i : curr_vec)
        i = 1;
    while(*f != '\0') {
        jh_update_vec(curr_vec, *f);
        new_type = jh_detect_type(curr_vec);
        if (new_type >= 0) {
            old_type = new_type;
            old_f = f;
        } else if (new_type == -1) {
            *type = old_type;
            return old_f + 1;
        }
        ++f;
    }
    *type = old_type;
    return old_f + 1;
}
